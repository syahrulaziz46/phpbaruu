          <p>
            Sejarah Singkat Reog Ponorogo telah menjadi kebanggaan masyarakat Kabupaten Ponorogo sejak jaman dahulu hingga sekarang ini. Reog Ponorogo sudah berumur lebih dari 2 abad. Hal itu dapat dilihat dari naskah Kanjeng Gusti Pangeran Adipati Anom Amengkunagara III Ing Surakarta, Pupuh di Serat Centini. Namun, menurut menurut legenda, keberadaan seni reog sudah ada sejak masa Kerajaan Kediri abad XI. Hal ini terkait cerita prosesi lamaran Prabu Kelanasewandana dari Kerajaan Wengker kepada Dewi Sanggalangit dari Kerajaan Kediri. Legenda ini dianggap sebagai versi sejarah tertua dari reog. Hingga saat ini cerita legenda Prabu Kelanasewandana tersebut masih dijadikan sebagai salah satu alur cerita dari seni pertunjukan reog di Ponorogo. 
